def style_wfs_layer(layer: QgsVectorLayer):
    if not isinstance(layer, QgsVectorLayer) or not layer.isValid():
        return

    geom_type = layer.geometryType()
    layer_name = layer.name().upper()

    # SEGMENT styling (categorized by c_synergie)
    if "SEGMENT" in layer_name:
        categories = []

        for synergy in SYNERGIE_VALUES:
            sym = QgsSymbol.defaultSymbol(geom_type)

            line_layer = QgsSimpleLineSymbolLayer()
            line_layer.setColor(QColor("red"))
            line_layer.setWidth(1.5)

            line_layer.setDataDefinedProperty(
                QgsSymbolLayer.PropertyStrokeStyle,
                QgsProperty.fromExpression(
                    "CASE WHEN \"id_segment_type\" = 3 THEN 'dash' ELSE 'solid' END"
                )
            )

            sym.changeSymbolLayer(0, line_layer)

            # Correct order: (value, symbol, label)
            categories.append(
                QgsRendererCategory(synergy, sym, f"Synergy ({synergy})")
            )

        # Non-synergy segments
        sym_gray = QgsSymbol.defaultSymbol(geom_type)

        gray_layer = QgsSimpleLineSymbolLayer()
        gray_layer.setColor(QColor("gray"))
        gray_layer.setWidth(0.8)

        gray_layer.setDataDefinedProperty(
            QgsSymbolLayer.PropertyStrokeStyle,
            QgsProperty.fromExpression(
                "CASE WHEN \"id_segment_type\" = 3 THEN 'dash' ELSE 'solid' END"
            )
        )

        sym_gray.changeSymbolLayer(0, gray_layer)

        categories.append(
            QgsRendererCategory("NON_SYNERGY", sym_gray, "Non-synergy")
        )

        renderer = QgsCategorizedSymbolRenderer("c_synergie", categories)
        renderer.setClassAttribute(
            "CASE WHEN \"c_synergie\" IS NULL OR \"c_synergie\" = 'Non' "
            "THEN 'NON_SYNERGY' ELSE \"c_synergie\" END"
        )

        layer.setRenderer(renderer)
        layer.triggerRepaint()

    # DUCT styling (categorized by Feature + c_synergie)
    elif "DUCT" in layer_name:
        name_field = "name"
        categories = []

        unique_names = sorted({
            f[name_field]
            for f in layer.getFeatures()
            if f[name_field] is not None
        })

        for name in unique_names:
            color = DUCT_COLORS.get(name, QColor("gray"))

            sym = QgsSymbol.defaultSymbol(geom_type)
            sym.setColor(color)

            if geom_type == QgsWkbTypes.LineGeometry:
                sym.setWidth(1.0)
            elif geom_type == QgsWkbTypes.PointGeometry:
                sym.setSize(2.0)

            # Correct order: (value, symbol, label)
            categories.append(
                QgsRendererCategory(name, sym, str(name))
            )

        # NULL / unknown ducts
        sym_gray = QgsSymbol.defaultSymbol(geom_type)
        sym_gray.setColor(QColor("gray"))
        categories.append(
            QgsRendererCategory(None, sym_gray, "Unknown duct")
        )

        renderer = QgsCategorizedSymbolRenderer(name_field, categories)
        layer.setRenderer(renderer)
        layer.triggerRepaint()

    # STRUCTURE styling (single green symbol)
    elif "STRUCTURE" in layer_name:
        fid_type_field = "fid_type"
        categories = []

        for fid, color in STRUCTURE_COLORS.items():
            sym = QgsSymbol.defaultSymbol(geom_type)
            sym.setColor(color)
            if geom_type == QgsWkbTypes.LineGeometry:
                sym.setWidth(1.0)
            elif geom_type == QgsWkbTypes.PointGeometry:
                sym.setSize(2.0)
            # Correct order: (value, symbol, label)
            categories.append(QgsRendererCategory(fid, sym, STRUCTURE_TYPE_MAP.get(fid, str(fid))))

        # Unknown types
        sym_gray = QgsSymbol.defaultSymbol(geom_type)
        sym_gray.setColor(QColor("gray"))
        categories.append(QgsRendererCategory(None, sym_gray, "Unknown"))

        renderer = QgsCategorizedSymbolRenderer(fid_type_field, categories)
        layer.setRenderer(renderer)
        layer.triggerRepaint()

def _layer_display_name(layer) -> Optional[str]:
    """Return a usable display name for a QGIS layer (calls .name() if callable)."""
    try:
        if layer is None:
            return None
        # prefer the callable .name()
        name_attr = getattr(layer, "name", None)
        if callable(name_attr):
            try:
                return name_attr()
            except Exception:
                pass
        # if name attribute exists but is not callable, convert to string
        if name_attr is not None:
            return str(name_attr)
        # fallback to source or id
        src = getattr(layer, "source", None)
        if callable(src):
            try:
                return src()
            except Exception:
                pass
        if src is not None:
            return str(src)
    except Exception:
        pass
    return None


def get_auth_id_for_provider(provider_name: str) -> Optional[str]:
    """
    Return the QGIS AuthConfig ID for a given provider name,
    based on the name stored in QGIS Auth Manager.
    """
    EXPECTED_AUTH_NAMES = {
        "FIBERKLAAR": "TKI FIBERKLAAR",
        "UNIFIBER": "UNIFIBER TKI",
        "WYRE": "TKI WYRE",
        "PROXIMUS": "UNIFIBER TKI",
    }

    auth_name = EXPECTED_AUTH_NAMES.get(provider_name.upper())
    if not auth_name:
        return None

    auth_mgr = QgsApplication.authManager()

    # `availableAuthMethodConfigs()` returns {authcfg_id: QgsAuthMethodConfig}
    configs = auth_mgr.availableAuthMethodConfigs()

    for auth_id, cfg in configs.items():
        if cfg.name() == auth_name:
            return auth_id

    return None

