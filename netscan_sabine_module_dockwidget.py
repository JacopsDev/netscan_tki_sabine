def extract_wfs_typename(layer: QgsVectorLayer) -> Optional[str]:
    try:
        src = layer.source()
        for part in src.split("&"):
            if part.lower().startswith("typename="):
                return part.split("=", 1)[1]
    except Exception:
        pass
    return None


