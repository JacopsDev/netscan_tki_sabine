    def classify_layer(self, layer: QgsVectorLayer) -> Tuple[Optional[str], Optional[str]]:
        """
        Return (provider, layer_type) based on layer name or WFS typename.
        """
        if layer is None or not isinstance(layer, QgsVectorLayer):
            return None, None

        lname = layer.name().lower()
        provider = None
        layer_type = None

        # Provider detection
        if "unifiber" in lname or "net_scan_jacops:" in lname:
            provider = "UNIFIBER"
        elif "wyre" in lname or "net_scan_jacops_wyre:" in lname:
            provider = "WYRE"
        elif "fiberklaar" in lname or "net_scan_fiberklaar:" in lname:
            provider = "FIBERKLAAR"
        elif "proximus" in lname or "net_scan_proximus:" in lname:
            provider = "PROXIMUS"

        # Layer type detection
        if "duct" in lname:
            layer_type = "DUCT"
        elif "structure" in lname:
            layer_type = "STRUCTURE"
        elif "segment" in lname:
            layer_type = "SEGMENT"
        elif "scan" in lname:
            layer_type = "SCAN"

        # Fallback: try WFS typename if layer_type is still unknown
        if layer_type is None:
            src = getattr(layer, "source", "")
            if isinstance(src, str):
                if "duct" in src.lower():
                    layer_type = "DUCT"
                elif "structure" in src.lower():
                    layer_type = "STRUCTURE"
                elif "segment" in src.lower():
                    layer_type = "SEGMENT"
                elif "scan" in src.lower():
                    layer_type = "SCAN"
                else:
                    layer_type = "UNKNOWN"

        if provider is None:
            provider = "UNKNOWN"

        return provider, layer_type


    def on_polygon_found(self, summary):
        text_widget = getattr(self, "txtUnifiber", None)
        if text_widget is None:
            print(">>> ERROR: txtUnifiber not initialized!")
            return

        text_widget.clear()

        if not summary:
            text_widget.append("<i>No features found in polygon.</i>")
            return

        # Group summary by provider
        provider_dict = {}
        for key, info in summary.items():
            provider = info.get("provider", "UNKNOWN")
            layer_type = info.get("layer_type", "UNKNOWN")
            details = info.get("details", {})
            provider_dict.setdefault(provider, {})[layer_type] = details

        for provider, layers in provider_dict.items():
            # Provider header
            text_widget.append(f'<b style="font-size:12pt; color:#2A52BE;">{provider}</b>')

            for layer_type in ["SEGMENT", "DUCT", "STRUCTURE", "SCAN", "None"]:
                if layer_type not in layers:
                    continue

                details = layers[layer_type]

                # Layer type header
                text_widget.append(f'<b style="color:#228B22;">{layer_type}</b>')

                if not details:
                    text_widget.append('<i style="margin-left:10px;">(no features)</i>')
                    continue

                # List features inline but block style to control spacing
                for fid, val in details.items():
                    if layer_type == "DUCT":
                        text_widget.append(
                            f'<span style="display:block; margin-left:10px;">{fid}: {float(val):.2f} m</span>')
                    else:
                        text_widget.append(f'<span style="display:block; margin-left:10px;">{fid}: {int(val)}</span>')

            # Optional small separator
            text_widget.append('<br>')

    def _checked_provider_names(self) -> list:
        """Return list of provider names checked in the UI, robust to widget type."""
        combo = getattr(self, 'mComboBox', None)
        if combo is None:
            return []

        names = []
        # preferred API
        if hasattr(combo, 'checkedItems'):
            try:
                for it in combo.checkedItems():
                    names.append(str(it))
                return names
            except Exception:
                pass

        # fallback: iterate items and inspect check state
        try:
            count = combo.count()
        except Exception:
            return names

        for i in range(count):
            checked = False
            if hasattr(combo, 'itemChecked'):
                try:
                    checked = combo.itemChecked(i)
                except Exception:
                    checked = False
            if not checked:
                try:
                    index = combo.model().index(i, 0)
                    checked = (index.data(Qt.CheckStateRole) == Qt.Checked)
                except Exception:
                    checked = False
            if not checked:
                continue

            try:
                text = combo.itemText(i)
            except Exception:
                try:
                    text = combo.model().index(i, 0).data(Qt.DisplayRole)
                except Exception:
                    text = ''
            if text:
                names.append(str(text))
        return names

