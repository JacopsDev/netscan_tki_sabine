    def load_selected_layers(self):
        # (existing provider selection and uri setup code unchanged)
        checked_items = self.mComboBox.checkedItems()
        if not checked_items:
            QtWidgets.QMessageBox.information(self, "Load layers", "No providers selected.")
            return


        for provider in checked_items:
            provider_upper = provider.upper()
            if provider_upper not in WFS_SERVICES:
                continue

            svc = WFS_SERVICES[provider_upper]
            url = svc["url"]
            auth_id = get_auth_id_for_provider(provider_upper)

            for typename in svc["typenames"]:
                uri = QgsDataSourceUri()
                uri.setParam("service", "WFS")
                uri.setParam("version", "2.0.0")
                uri.setParam("request", "GetFeature")
                uri.setParam("typename", typename)
                uri.setParam("srsName", "EPSG:31370")
                uri.setParam("url", url)
                if auth_id:
                    uri.setAuthConfigId(auth_id)

                # Use custom display name when available
                layer_name = custom_names.get(typename, typename.split(":")[-1])
                try:
                    vl = QgsVectorLayer(uri.uri(), layer_name, "WFS")
                    if not vl.isValid():
                        QtWidgets.QMessageBox.warning(self, "Load WFS", f"Failed to load {layer_name}")
                    else:
                        QgsProject.instance().addMapLayer(vl)
                        # Immediately style the layer
                        style_wfs_layer(vl)
                except Exception as e:
                    QtWidgets.QMessageBox.warning(self, "Load WFS", f"Error loading {layer_name}: {e}")
        try:
            # Ensure base map is added after WFS layers
            self.add_layer_wms()
        except Exception:
            pass

        QtWidgets.QMessageBox.information(self, "Load WFS", "Finished loading selected layers.")

