    def finish_polygon(self):
        print(">>> finish_polygon CALLED")
        QgsMessageLog.logMessage("finish_polygon CALLED", "netscan_sabine", Qgis.Info)
        segment_layer = self._layer_from_id(self.segment_layer_id)
        duct_layer = self._layer_from_id(self.duct_layer_id)
        structure_layer = self._layer_from_id(self.structure_layer_id)
        #
        SEGMENT_TYPE_MAP = {
            1: "Open sleuf",
            2: "Handboring",
            3: "Gestuurde boring",
        }
        STRUCTURE_TYPE_MAP = {
            # 11202: "Grube",
            # 11203: "AZK (Neu)",
            # 11204: "Kabelschacht (Neu)",
            # 11205: "AZK (Bestand)",
            # 11206: "Kabelschacht (Bestand)",
            11209: "POC",
            11210: "EK478",
            11211: "Has-Koppeling",
            11212: "Koppeling",
            11213: "MF endbox",
            11214: "Riser",
            11215: "Toby-Box (type HIB)",
            11216: "Other",
            3906712: "EK578",
            3906715: "Modulaire Betonbak",
            3906716: "Kunstof-lasput (type LOT)",
            3906722: "Handhole (POC)",
            3906723: "Medium Bak (DP)",
            3906724: "Standard Bak (PDP)",
            4962341: "STOP",
            4962342: "Koppelbak",
            4962343: "PDP Beton",
            4962344: "Sokkel POP",
            4962345: "PDP Kunststof",
            4962346: "POC versterkt",
            4962347: "Sokkelpop",
        }

        if len(self.points) < 3:
            print(">>> Not enough points to form polygon")
            self.reset()
            return

        # Close polygon
        pts = list(self.points)
        if pts[0] != pts[-1]:
            pts.append(pts[0])
        poly = QgsGeometry.fromPolygonXY([pts])
        map_crs = self.canvas.mapSettings().destinationCrs()

        summary: dict[str, dict] = {}

        # Collect candidate layers
        candidate_layers = []

        explicit_layers = [segment_layer, duct_layer, structure_layer]
        for el in explicit_layers:
            if isinstance(el, QgsVectorLayer):
                candidate_layers.append(el)

        ALLOWED_LAYER_KEYWORDS = ("unifiber", "wyre", "fiberklaar", "proximus")

        for lyr in QgsProject.instance().mapLayers().values():
            if not isinstance(lyr, QgsVectorLayer) or not lyr.isValid():
                continue

            lname = lyr.name().lower().strip()
            if not any(keyword in lname for keyword in ALLOWED_LAYER_KEYWORDS):
                continue

            if lyr not in candidate_layers:
                candidate_layers.append(lyr)

            QgsMessageLog.logMessage(
                f"Accepted layer: {lyr.name()}",
                "netscan_sabine",
                Qgis.Info
            )

        if not candidate_layers:
            print(">>> No candidate layers found")
            if self.on_found:
                self.on_found(summary)
            self.reset()
            return

        # Process layers
        for layer in candidate_layers:
            if not isinstance(layer, QgsVectorLayer) or not layer.isValid():
                continue

            provider, layer_type = getattr(self, 'classify_layer', lambda l: (None, None))(layer)

            # --- FALLBACK detection based on layer name ---
            if provider is None:
                provider = layer.name().split()[0]  # e.g., 'Wyre', 'Fiberklaar', etc.
            if layer_type is None:
                lname = layer.name().lower()
                if lname.startswith("UNKNOWN"):
                    continue
                elif "duct" in lname:
                    layer_type = "DUCT"
                elif "structure" in lname:
                    layer_type = "STRUCTURE"
                elif "segment" in lname:
                    layer_type = "SEGMENT"


            key = f"{provider}:{layer_type}"
            if key not in summary:
                summary[key] = {"provider": provider, "layer_type": layer_type, "details": {}}
            details = summary[key]["details"]

            # Transform polygon to layer CRS if needed
            check_poly = QgsGeometry(poly)
            try:
                if map_crs != layer.crs():
                    transform = QgsCoordinateTransform(map_crs, layer.crs(), QgsProject.instance())
                    check_poly.transform(transform)
            except Exception as e:
                QgsMessageLog.logMessage(f"{layer.name()}: CRS transform failed: {e}", "netscan_sabine", Qgis.Warning)
                continue

            # Fetch features intersecting polygon
            try:
                request = QgsFeatureRequest().setFilterRect(check_poly.boundingBox())
                features = list(layer.getFeatures(request))
            except Exception:
                features = list(layer.getFeatures())

            for f in features:
                geom = f.geometry()
                if not geom:
                    continue
                try:
                    if not geom.intersects(check_poly):
                        continue
                except Exception:
                    continue
                # -----------------------
                # HIGHLIGHT FEATURE
                # -----------------------
                if layer_type == "SEGMENT":
                    # Red, dashed crossings are already styled → thicker highlight
                    is_crossing = (
                            "id_segment_type" in f.fields().names()
                            and f["id_segment_type"] == 3
                    )
                    self.highlight_feature(
                        layer,
                        f,
                        QColor(255, 0, 0, 180),
                        width=4 if is_crossing else 2
                    )

                elif layer_type == "DUCT":
                    self.highlight_feature(
                        layer,
                        f,
                        QColor(0, 0, 255, 180),
                        width=2
                    )

                elif layer_type == "STRUCTURE":
                    self.highlight_feature(
                        layer,
                        f,
                        QColor(0, 180, 0, 200),
                        width=4
                    )
                # --- accumulate totals ---
                if layer_type == "DUCT":
                    name = next(
                        (f[field] for field in ["name", "duct_name", "id", "fid"] if field in f.fields().names()),
                        "UNKNOWN")
                    details[name] = details.get(name, 0.0) + float(geom.length())
                elif layer_type == "STRUCTURE":


                    field_name = None
                    for candidate in ("fid_type", "structure_type", "id"):
                        if candidate in f.fields().names():
                            field_name = candidate
                            break

                    if field_name:
                        raw = f[field_name]
                        try:
                            raw_int = int(raw)
                        except Exception:
                            raw_int = None

                        label = STRUCTURE_TYPE_MAP.get(raw_int, f"UNKNOWN ({raw})")
                    else:
                        if label.startswith("UNKNOWN"):
                            continue
                    details[label] = details.get(label, 0) + 1

                elif layer_type == "SEGMENT":
                    if "id_segment_type" in f.fields().names():
                        raw = f["id_segment_type"]
                        label = SEGMENT_TYPE_MAP.get(raw, f"UNKNOWN ({raw})")
                    else:
                        label = "UNKNOWN"

                    details[label] = details.get(label, 0) + 1

                else:
                    fid = str(f.id())
                    details[fid] = details.get(fid, 0) + 1

        # Callback with results
        if self.on_found:
            try:
                self.on_found(summary)
            except Exception:
                QgsMessageLog.logMessage("on_found callback failed", "netscan_sabine", Qgis.Warning)
        else:
            print(">>> self.on_found is None")

        # Restore previous map tool
        if getattr(self, "prev_map_tool", None):
            try:
                self.canvas.setMapTool(self.prev_map_tool)
            except Exception:
                pass

        if self.btn_select:
            self.btn_select.setStyleSheet("")  # reset color

        # Restore button color
        self.reset_drawing()


    def _layer_from_id(self, layer_id):
        if not layer_id:
            return None
        layer = QgsProject.instance().mapLayer(layer_id)
        if not isinstance(layer, QgsVectorLayer):
            return None
        return layer

    def reset_drawing(self):
        self.drawing = False
        self.points = []

        if self.rubber:
            try:
                self.rubber.reset(QgsWkbTypes.PolygonGeometry)
            except Exception:
                self.rubber.reset()
            try:
                self.rubber.hide()
            except Exception:
                pass

    def highlight_feature(self, layer: QgsVectorLayer, feature, color: QColor, width=2):
        try:
            h = QgsHighlight(self.canvas, feature.geometry(), layer)
            h.setColor(color)
            h.setWidth(width)
            h.show()
            self.highlights.append(h)
        except Exception:
            pass

    def deactivate(self):
        self.clear_highlights()
        try:
            super().deactivate()
        except Exception:
            pass
        # Reset button color when tool is deactivated
        if hasattr(self, 'btn_select') and self.btn_select:
            self.btn_select.setStyleSheet("")

    def clear_highlights(self):
        for h in getattr(self, "highlights", []):
            try:
                h.hide()
            except Exception:
                pass
            try:
                h.deleteLater()
            except Exception:
                pass
        self.highlights = []

    def reset(self):
        self.drawing = False
        self.points = []
        self.clear_highlights()

        self.reset_drawing()

        if self.rubber:
            try:
                self.rubber.reset(QgsWkbTypes.PolygonGeometry)
            except Exception:
                self.rubber.reset()
            try:
                self.rubber.hide()
            except Exception:
                pass

