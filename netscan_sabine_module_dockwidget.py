    def add_layer_wms(self):

        tms = 'type=xyz&url=https://tile.openstreetmap.org/{z}/{x}/{y}.png&zmax=19&zmin=0'
        layer_name = 'OSM'

        # avoid duplicates
        if QgsProject.instance().mapLayersByName(layer_name):
            QgsMessageLog.logMessage("OSM layer already exists, skipping.", "netscan_sabine", Qgis.Info)
            return

        slayer = QgsRasterLayer(tms, layer_name, 'wms')
        if not slayer.isValid():
            QgsMessageLog.logMessage("Failed to create OSM layer (invalid).", "netscan_sabine", Qgis.Warning)
            return

        # register the layer without adding it to the layer tree
        QgsProject.instance().addMapLayer(slayer, False)

        root = QgsProject.instance().layerTreeRoot()
        try:
            # insert at the bottom (so other layers added later appear above it)
            root.insertLayer(len(root.children()), slayer)
            QgsMessageLog.logMessage("OSM layer added at bottom of layer tree.", "netscan_sabine", Qgis.Info)
        except Exception as ex:
            # fallback: add normally if insert fails
            QgsProject.instance().addMapLayer(slayer)
            QgsMessageLog.logMessage(f"Insert at bottom failed ({ex}); added normally.", "netscan_sabine", Qgis.Warning)



